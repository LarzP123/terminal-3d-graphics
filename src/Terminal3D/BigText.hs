module Terminal3D.BigText where

import Data.Char

-- | Print big text at a given scale.
printBig :: Int -> String -> IO ()
printBig 0 msg = putStrLn msg
printBig scale msg = mapM_ putStrLn rows
  where
    chars      = map (glyph . toUpper) msg
    rows       = concatMap expandRow [0..6]
    expandRow r = replicate scale $ concatMap (\g -> expandCol (g !! r) ++ replicate scale ' ') chars
    expandCol = concatMap (replicate (scale * 2))

